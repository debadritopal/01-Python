def addition(num1, num2):
    return num1+num2
def subtraction(num1, num2):
    return num1-num2
def multiplication(num1, num2):
    return num1*num2
def division(num1, num2):
    return num1/num2
print(f"Addition: {addition(10,20)}")
print(f"Subtraction: {subtraction(10,20)}")
print(f"Multiplication: {multiplication(10,20)}")
print(f"Division: {division(10,20)}")